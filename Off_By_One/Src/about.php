<h3 tabindex="0">Steve Mannheimer</h3>
<p tabindex="0" class="text-left">Steve Mannheimer has been a faculty member at the Herron School of Art, IUPUI, since 1976. In the summer of 2000, he
    began a two-year leave of absence from IUPUI to become a full-time employee of Thomson Multimedia, Inc. Within
    Thomson's New Media Services division, he was part of a multi-disciplinary team developing new media business
    concepts, strategies, business plans and technologies. His team worked in collaboration with other Thomson divisions
    as well as with other research facilities around the world. While employed with Thomson, Mannheimer's work included
    invention of patentable technologies and applications, resulting in over 20 patent disclosures filed either
    individually or as part of the team.
    <br>
    In the spring of 2002, while still with Thomson, Mannheimer taught a seminar course for the Media Arts and Science
    program in Imaging in Digital Media. In the summer of 2002, he returned to a full-time academic career, joining the
    Media Arts and Science program in the School of Informatics and Computing.
    <br>
    In addition to his activities as a studio artist and university professor, Mannheimer has been an art journalist,
    writing a weekly column on art and architecture for The Indianapolis Star for nearly 20 years. He has also been
    involved in the civic promotion of art, having directed several large-scale public art projects in Indianapolis,
    including the memorial to Dr. Martin Luther King, Jr. and Sen. Robert Kennedy in the city's King Memorial Park.</p>
